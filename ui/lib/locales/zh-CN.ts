/**
 * Compiler shape used by ./en.ts — `DeepString<typeof zhCN>` widens every
 * string literal to `string` so the English bundle's values don't have to
 * match the Chinese ones character-for-character; keys + nesting must
 * still line up.
 */
type DeepString<T> = {
  [K in keyof T]: T[K] extends string ? string : DeepString<T[K]>;
};

export type LocaleBundle = DeepString<typeof zhCN>;
